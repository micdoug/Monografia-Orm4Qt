//Declaração de um vetor
int vetor[] = {1, 2, 3, 4, 5};
//Acesso sequencial via índice
for (int i = 0; i < 5; ++i) {
    cout << vetor[i] << endl;
}

//Declaração de um container
vector<int> container = {1, 2, 3, 4, 5};
//Acesso sequencial via índice
for (int i = 0; i < container.size(); ++i) {
    cout << container[i] << endl;
}